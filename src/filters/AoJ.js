
export const AoJ = (str) => {
  switch (str) {
    case 'INCOME':
      return '收入';
      break;
    case 'RA':
      return '交易归还资金';
      break;
    case 'T':
      return '解冻';
      break;
    case 'R':
      return '充值';
      break;
    case 'REWARD':
      return '奖励';
      break;
    case 'DEFRAY':
      return '支出';
      break;
    case 'BOFP':
      return '交易划钱';
      break;
    case 'F':
      return '冻结';
      break;
    case 'B':
      return '交易买';
      break;
    case 'S':
      return '交易卖';
      break;
    case 'W':
      return '提现';
      break;
    case 'TRADE_FEE':
      return '交易手续费';
      break;
    case 'RECHARGE_FEE':
      return '充值手续费';
      break;
    case 'WITHDRAWALS_FEE':
      return '充值手续费';
      break;
  }
}
